    <h4>Milagros verificados por la iglesia que permitieron su beatifiacacion</h4>
    <p>El primer milagro concedido fue el concedido a Elvira Moriano, una anciana a quien los médicos le habían dicho que perdería la visión del ojo derecho debido a una herida provocada cuando chocó contra una ventana. De acuerdo con Celia Cussen, profesora de la Universidad de Chile en el departamento de ciencias históricas, un padre dominico le envió una reliquia de San Martín de Porres y le pidió que se encomendara a él. Su ojo sanó al día siguiente. Una veintena de testigos dieron por verdadero el hecho.</p>
