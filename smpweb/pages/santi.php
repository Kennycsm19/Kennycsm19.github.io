    <h4>Milagros verificados por la iglesia que permitieron su canonización</h4>
    <p>En 1948 el primer milagro que se verificó para ser santo ocurrió en Paraguay. Una anciana de 89 años había sufrido un infarto y los doctores le pronosticaron pocas horas de vida. Su hija, que se encontraba en Argentina, le rezaba al santo limeño por la salud de su madre. La familia inició los arreglos de su funeral, pero, al día siguiente, la octogenaria mujer despertó sana.</p>
