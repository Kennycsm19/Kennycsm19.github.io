        <li><h5>Sanación</h5></li>
        <p>De acuerdo a los relatos de la época, en ocasiones San Martín de Porres lograba curaciones instantáneas, mientras que en otros casos su presencia era suficiente para que los enfermos desahuciados iniciaran un notable y firme proceso de recuperación. Por lo general, los remedios que él indicaba eran los apropiados para cada caso, aunque en algunas ocasiones, cuando no los tenía a su disposición, recurría a métodos inverosímiles que, sin embargo, daban resultados similares. Por ejemplo, logró sanar a un niño que se había fracturado ambas piernas utilizando unas vendas y vino tibio, o aplicando un trozo de suela de zapato en el brazo de un zapatero para sanar una grave infección. San Martín solía decir la frase "Yo te curo, Dios te sana" para evitar cualquier muestra de veneración hacia su persona.</p>
        <li><h5>Sanación</h5></li>
        <p>De acuerdo a los testimonios de la época, San Martín de Porres se le apareció a personas enfermas y moribundas en México, Filipinas, China, Japón y África. Su presencia en la cama de estos pacientes les daba consuelo y les proporcionaba la fuerza necesaria para recuperarse. Asimismo, se le aparecía a misioneros que enfrentaban dificultades para brindarles ayuda.</p>
        <li><h5>Levitación</h5></li>
        <p>Según varios testimonios, en ocasiones cuando San Martín de Porres oraba con gran devoción, parecía levitar en el aire y perdía la noción del entorno y de las personas a su alrededor, concentrándose únicamente en su oración.</p>
