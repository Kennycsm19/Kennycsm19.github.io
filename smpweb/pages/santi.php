    <p>El segundo milagro, comprobado por al menos cinco personas, fue el del niño Melchor Varanda. Él cayó del techo de su casa en Lima y se rompió el cráneo. Los médicos no le daban esperanzas de vida; no obstante, su madre lo encomendó a San Martín de Porres y, un día después de este diagnóstico, el pequeño se levantó como si nada hubiera pasado.</p>
