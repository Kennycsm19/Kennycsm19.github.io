      <p>El Papa Juan XXIII sentía una verdadera devoción por Martin de Porres, que lo canonizó en la ciudad del Vaticano el 6 de mayo de ese año ante una multitud de 40,000 personas de todas partes del mundo y lo nombró santo patrón de la justicia social con las siguientes palabras:</p>
      <p>«San Martín, siempre obediente e inspirado por su divino Maestro, vivió entre sus hermanos con ese amor profundo que nace de la fe pura y de la humildad de corazón. Amaba a los hombres porque los veía como hijos de Dios y como sus propios hermanos y hermanas. Tal era su humildad que los amaba más que a sí mismo, y que los consideraba mejores y más virtuosos que él... Martín excusaba las faltas de otro. Perdonó las más amargas injurias, convencido de que el merecía mayores castigos por sus pecados. Procuró de todo corazón animar a los acomplejados por las propias culpas, confortó a los enfermos, proveía de ropas, alimentos y medicinas a los pobres, ayudó a campesinos, a negros y mulatos tenidos entonces como esclavos. La gente le llama Martín, el bueno».</p>
