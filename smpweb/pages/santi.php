    <p>Años después , en 1956 en Tenerife España, un niño de nombre Antonio Cabrera Pérez estaba a punto de perder su pierna izquierda debido a una gangrena. Un amigo de la familia entregó una reliquia y una imagen de San Martín de Porres a la madre. Ella pasó ambos objetos por encima de la pierna del menor y rezó para que no la perdiera. Luego de dos días de intensas oraciones, esta volvió a su estado natural.</p>
